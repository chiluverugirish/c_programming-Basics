#include<stdio.h>
int main(){
    int x=5;
    printf("%d\n",x);
    x=x+6;//here to comment use front slash
    //x is carry forwarded next problems
    printf("%d\n",x);
    x++;//x=x+1 shortcut and called post increment
    printf("%d\n",x);
    x=x*8;
    printf("%d\n",x);
    --x;//x=x-1,called pre decrement
    printf("%d",x);
    return 0;
    
    





}